class Area_Of_Circle{
	public static void main(String args[]){
	int r=5;
	double pi=3.14;
	double A;
	
	// // Calculate the area of the circle
	A = pi * r * r;

	//Print the values
	System.out.println("The area of Circle is : "+A);
	
}
}